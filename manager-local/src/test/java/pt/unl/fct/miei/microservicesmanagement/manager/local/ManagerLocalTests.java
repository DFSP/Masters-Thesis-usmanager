package pt.unl.fct.miei.microservicesmanagement.manager.local;

import org.junit.jupiter.api.Test;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.context.SpringBootTest;
import pt.unl.fct.miei.microservicesmanagement.manager.local.kafka.Customer;
import pt.unl.fct.miei.microservicesmanagement.manager.local.mongo.CustomerRepository;

@SpringBootTest
class ManagerLocalTests {

  @Autowired
  private CustomerRepository repository;

  @Test
  public void databaseTest() {

    repository.deleteAll();

    // save a couple of customers
    repository.save(new Customer("Alice", "Smith"));
    repository.save(new Customer("Bob", "Smith"));

    // fetch all customers
    System.out.println("Customers found with findAll():");
    System.out.println("-------------------------------");
    for (Customer customer : repository.findAll()) {
      System.out.println(customer);
    }
    System.out.println();

    // fetch an individual customer
    System.out.println("Customer found with findByFirstName('Alice'):");
    System.out.println("--------------------------------");
    System.out.println(repository.findByFirstName("Alice"));

    System.out.println("Customers found with findByLastName('Smith'):");
    System.out.println("--------------------------------");
    for (Customer customer : repository.findByLastName("Smith")) {
      System.out.println(customer);
    }

  }

  @Test
  void contextLoads() {
  }

}
